CREATE TABLE `gm_process_info` (
  `ID` BIGINT PRIMARY KEY AUTO_INCREMENT,
  `PROCESS_INSTANCE_ID` VARCHAR(255) NOT NULL COMMENT '流程实例id',
  `CREATE_DATE` DATETIME NOT NULL COMMENT '创建时间',
  `CREATE_USER` VARCHAR(50) DEFAULT NULL COMMENT '创建者',
  `TYPE` VARCHAR(50) NOT NULL COMMENT '流程类型',
  `NAME` VARCHAR(50) DEFAULT NULL COMMENT '流程名称',
  `STATUS` INT NOT NULL COMMENT '状态',
  `REMARK` VARCHAR(500) DEFAULT NULL COMMENT '流程描述',
  KEY `INDEX_PROCESS_INSTANCE_ID` (`PROCESS_INSTANCE_ID`)
) ENGINE=INNODB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

CREATE TABLE `gm_task_info` (
  `ID` bigint NOT NULL AUTO_INCREMENT,
  `TASK_ID` varchar(255) COLLATE utf8mb4_general_ci NOT NULL COMMENT '任务id',
  `COMPLETE_DATE` datetime NOT NULL COMMENT '任务完成时间',
  `OPERATE_TYPE` tinyint NOT NULL COMMENT '操作类型',
  `OPERATE_NAME` varchar(50) COLLATE utf8mb4_general_ci DEFAULT NULL COMMENT '操作名称',
  `OPERATOR` varchar(50) COLLATE utf8mb4_general_ci DEFAULT NULL COMMENT '操作人',
  `ATTACHMENT_INFO` varchar(5000) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci DEFAULT NULL COMMENT '任务附属信息',
  `variables` varchar(5000) COLLATE utf8mb4_general_ci DEFAULT NULL COMMENT '参数',
  PRIMARY KEY (`ID`),
  KEY `INDEX_TASK_ID` (`TASK_ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1991394430455500803 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci
